      return(NULL)
    }

    if (!is.null(no_embd_to_use)) {
      to_take <- min(c(no_embd_to_use, ncol(embd)))
      embd <- embd[, 1:to_take]
    }
    knn_data <- NULL
  }

  supercell_res <- rs_supercell(
    f_path = get_rust_count_cell_f_path(object),
