  var_data <- get_sc_var(object, cols = c("gene_idx", "gene_id"))

  meta_cell_obj <- meta_cells(
    meta_cell_data = supercell_res,
    var_data = var_data,
    meta_cell_method = "supercells"
  )

  return(meta_cell_obj)
